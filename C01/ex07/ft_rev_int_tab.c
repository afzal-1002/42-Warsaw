#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	mid;
	int	i;
	int	temp;

	i = 0;
	mid = size / 2;
	while (i < mid)
	{
		temp = tab[i];
		tab[i] = tab[size - i - 1];
		tab[size - i - 1] = temp;
		i++;
	}
}
